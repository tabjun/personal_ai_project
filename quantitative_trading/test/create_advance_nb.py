        "        self.fc = nn.Linear(d_model, 1)\n",
        "        \n",
        "    def forward(self, x):\n",
        "        x = self.embed(x)\n",
        "        x = self.mamba_block(x)\n",
        "        return self.fc(x[:, -1, :])\n",
