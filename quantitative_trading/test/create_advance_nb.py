        "# (1) Base LSTM 모델\n",
        "class LSTMModel(nn.Module):\n",
        "    def __init__(self, input_dim=1, hidden_dim=64, num_layers=2):\n",
        "        super(LSTMModel, self).__init__()\n",
        "        self.lstm = nn.LSTM(input_dim, hidden_dim, num_layers, batch_first=True, dropout=0.2 if num_layers > 1 else 0)\n",
        "        self.fc = nn.Linear(hidden_dim, 1)\n",
        "    def forward(self, x):\n",
        "        out, _ = self.lstm(x)\n",
        "        return self.fc(out[:, -1, :])\n",
        "\n",
        "# (2) TCN (Temporal Convolutional Network)\n",
        "class TCNBlock(nn.Module):\n",
        "    def __init__(self, in_channels, out_channels, kernel_size, dilation):\n",
        "        super(TCNBlock, self).__init__()\n",
        "        padding = (kernel_size - 1) * dilation\n",
        "        self.conv = nn.Conv1d(in_channels, out_channels, kernel_size, padding=padding, dilation=dilation)\n",
        "        self.relu = nn.ReLU()\n",
        "    def forward(self, x):\n",
        "        out = self.relu(self.conv(x))\n",
        "        return out[:, :, :-self.conv.padding[0]]\n",
        "\n",
        "class TCNModel(nn.Module):\n",
        "    def __init__(self, input_dim=1, num_channels=[32, 64], kernel_size=3):\n",
        "        super(TCNModel, self).__init__()\n",
        "        layers = []\n",
        "        in_channels = input_dim\n",
        "        for i in range(len(num_channels)):\n",
        "            dilation = 2 ** i\n",
        "            layers.append(TCNBlock(in_channels, num_channels[i], kernel_size, dilation))\n",
        "            in_channels = num_channels[i]\n",
        "        self.tcn = nn.Sequential(*layers)\n",
        "        self.fc = nn.Linear(num_channels[-1], 1)\n",
        "    def forward(self, x):\n",
        "        x = x.transpose(1, 2)\n",
        "        out = self.tcn(x)\n",
        "        return self.fc(out[:, :, -1])\n",
        "\n",
        "# (3) mTAND (간략화된 Continuous Time Attention 버전)\n",
        "class Simplified_mTAND(nn.Module):\n",
        "    def __init__(self, input_dim=1, embed_dim=16, num_heads=2):\n",
        "        super(Simplified_mTAND, self).__init__()\n",
        "        # 시계열 인덱스를 연속 시간 임베딩으로 매핑\n",
        "        self.time_embed = nn.Linear(1, embed_dim)\n",
        "        self.feature_embed = nn.Linear(input_dim, embed_dim)\n",
        "        self.attention = nn.MultiheadAttention(embed_dim, num_heads, batch_first=True)\n",
        "        self.fc = nn.Linear(embed_dim, 1)\n",
        "        \n",
        "    def forward(self, x):\n",
        "        batch_size, seq_len, _ = x.size()\n",
        "        # 가상의 시간 정보 생성 [batch, seq_len, 1]\n",
        "        time_steps = torch.arange(seq_len, dtype=torch.float32, device=x.device).unsqueeze(0).unsqueeze(-1).repeat(batch_size, 1, 1)\n",
        "        t_emb = self.time_embed(time_steps)\n",
        "        x_emb = self.feature_embed(x)\n",
        "        \n",
        "        # 시간 정보와 특징 정보를 결합\n",
        "        combined = t_emb + x_emb\n",
        "        attn_out, _ = self.attention(combined, combined, combined)\n",
        "        return self.fc(attn_out[:, -1, :])\n",
        "\n",
        "# (4) Mamba Block (시계열 예측을 위한 간략화된 SSM 아키텍처)\n",
        "class SimplifiedMambaBlock(nn.Module):\n",
        "    def __init__(self, d_model, d_state=16):\n",
        "        super(SimplifiedMambaBlock, self).__init__()\n",
        "        # 실제 Mamba는 하드웨어 최적화된 선택적 스캔(Selective Scan)을 사용하지만, \n",
        "        # 여기서는 개념을 증명하기 위해 선형 상태 공간 방정식의 근사 구조를 사용합니다.\n",
        "        self.fc1 = nn.Linear(d_model, d_model * 2)\n",
        "        self.conv = nn.Conv1d(d_model * 2, d_model * 2, kernel_size=3, padding=1)\n",
        "        self.fc2 = nn.Linear(d_model * 2, d_model)\n",
        "        \n",
        "        # Selective scan 파라미터 근사\n",
        "        self.dt = nn.Linear(d_model, d_state)\n",
        "        self.B = nn.Linear(d_model, d_state)\n",
        "        self.C = nn.Linear(d_model, d_state)\n",
        "        \n",
        "    def forward(self, x):\n",
        "        # x: [batch, seq_len, d_model]\n",
        "        x_proj = self.fc1(x)\n",
        "        x_conv = self.conv(x_proj.transpose(1, 2)).transpose(1, 2)\n",
        "        x_conv = nn.functional.silu(x_conv)\n",
        "        out = self.fc2(x_conv)\n",
        "        return out + x\n",
